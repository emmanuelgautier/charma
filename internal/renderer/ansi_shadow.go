const ansiShadowHeight = 6
const ansiShadowSpace = 3 // width of space character

// renderAnsiShadow renders text using the ansi_shadow style.
// Non-ASCII characters are rendered using the emoji glyph map.
func renderAnsiShadow(text string) []string {
	rows := make([]string, ansiShadowHeight)

	runes := []rune(text)
	for i := 0; i < len(runes); i++ {
		ch := runes[i]

		if isEmojiRune(ch) {
			// Consume continuation codepoints (variation selectors, ZWJ, etc.).
			for i+1 < len(runes) && isEmojiContinuation(runes[i+1]) {
				i++
			}
			glyph := getEmojiGlyph(ch, ansiShadowHeight)
			for r, line := range glyph {
				rows[r] += line
			}
			continue
		}

		upper := unicode.ToUpper(ch)
		glyph, ok := ansiShadowGlyphs[upper]
		if !ok {
			// Fallback: use a space-width blank glyph.
			for r := range rows {
				rows[r] += strings.Repeat(" ", ansiShadowSpace)
			}
			continue
		}
		for r, line := range glyph {
			rows[r] += line
		}
	}

	return rows
}
